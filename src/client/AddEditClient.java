    private void jDateChooserBirthdatePropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jDateChooserBirthdatePropertyChange
        if (((JTextField) jDateChooserBirthdate.getDateEditor().getUiComponent()).getText().equals("")) {
            //
        } else {
            String split[] = ((JTextField) jDateChooserBirthdate.getDateEditor().getUiComponent()).getText().split("-");
            int year = Integer.parseInt(split[0]);
            int month = Integer.parseInt(split[1]);
            int day = Integer.parseInt(split[2]);
            LocalDate birthdate = new LocalDate(year, month, day);
            LocalDate now = new LocalDate();
            Period period = new Period(birthdate, now, PeriodType.yearMonthDay());
            jTextFieldAge.setText(period.getYears() + " years and " + period.getMonths() + " months old.");
            jTextFieldMobileNo.requestFocusInWindow();
        }
    }//GEN-LAST:event_jDateChooserBirthdatePropertyChange

