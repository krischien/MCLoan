    private byte[] getImgBytes(Image image) {
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        try {
            ImageIO.write(getBufferedImage(image), "JPEG", baos);
        } catch (IOException ex) {
            ex.printStackTrace();
        }
        return baos.toByteArray();
    }

    private BufferedImage getBufferedImage(Image image) {
        BufferedImage bi = new BufferedImage(this.panel.getWidth(), this.panel.getHeight(),
                BufferedImage.TYPE_INT_ARGB);
        bi.getGraphics().drawImage(image, 0, 0, this.panel.getWidth(), this.panel.getHeight(), null);
        return bi;
    }

