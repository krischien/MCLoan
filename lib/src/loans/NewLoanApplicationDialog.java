    public void saveLoan() {
        loanLib.setClientId(getId());
        loanLib.setPnNo(jTextFieldAccountNo.getText().trim());
        loanLib.setMonthlyAmmortization(Double.parseDouble(jFormattedTextFieldMonthlyAmmortization.getText().trim()));
        loanLib.setMonthlyChange(Double.valueOf(jFormattedTextFieldMonthlyAmmortization.getText().trim()));
        loanLib.setLoanType(jComboBoxLoanType.getSelectedItem().toString().trim());
        loanLib.setLoanTerm(Double.parseDouble(jSpinnerTerm.getValue().toString()));
        loanLib.setGrossLoan(Double.parseDouble(jFormattedTextFieldGrossLoan.getText().trim()));
        loanLib.setInterests(Double.parseDouble(jFormattedTextFieldInterest.getText().trim()));
        loanLib.setPrincipal(Double.parseDouble(jFormattedTextFieldPrincipal.getText().trim()));
        loanLib.setProcessingFee(Double.parseDouble(jFormattedTextFieldProcessingFee.getText().trim()));
        loanLib.setOtherDeduction(Double.parseDouble(jFormattedTextFieldOtherDeduction.getText().trim()));
        loanLib.setAmoountOfCheck(Double.parseDouble(jFormattedTextFieldNetCashOut.getText().trim()));
        loanLib.setFirstDue(Date.valueOf(jFormattedTextFieldFirstDue.getText().trim()));
        loanLib.setLastDue(Date.valueOf(jFormattedTextFieldLastDue.getText().trim()));
        loanLib.setBranchId(1);
        loanLib.setActive(true);

        loanLib.insert();
        this.dispose();
    }
    
