    private void jButtonComputeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonComputeActionPerformed
        double pension = Double.parseDouble(jFormattedTextFieldMonthlyPension.getText().toString().replace(",", ""));
        double ammortization = Double.parseDouble(jFormattedTextFieldDesiredAmmortization.getText().toString().replace(",", ""));
        double deduction = Double.parseDouble(jFormattedTextFieldOtherDeduction.getText().toString().replace(",", ""));
        int term = Integer.parseInt(jSpinnerTerm.getValue().toString());

        if (jComboBoxLoanType.getSelectedItem().toString().equalsIgnoreCase("new")) {
            loanProcessorLib.processNewLoan(pension, ammortization, term, deduction);
        } else if (jComboBoxLoanType.getSelectedItem().toString().equalsIgnoreCase("Additional")) {
            loanProcessorLib.processAdditionalLoan(pension, ammortization, term, deduction, getId());
        } else if (jComboBoxLoanType.getSelectedItem().toString().equalsIgnoreCase("13th Month")) {
            loanProcessorLib.process13thMonthLoan(pension, ammortization, term, deduction);
        }
        loadInfo();
    }//GEN-LAST:event_jButtonComputeActionPerformed

